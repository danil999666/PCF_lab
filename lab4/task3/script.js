"" + 1 + 0 // "10" (конкатенація рядків)
"" - 1 + 0 // -1 (рядок "" перетворюється в число, тому отримуємо -1)
true + false // 1 (true перетворюється в 1, false в 0, отже 1 + 0 = 1)
6 / "3" // 2 (рядок "3" перетворюється в число, отримуємо 6 / 3 = 2)
"2" * "3" // 6 (рядок "2" і "3" перетворюються в числа, отримуємо 2 * 3 = 6)
4 + 5 + "px" // "9px" (спочатку виконується додавання чисел, отримуємо 9, а потім конкатенація з рядком "px")
"$" + 4 + 5 // "$45" (рядок "$" конкатенується з числами 4 і 5)
"4" - 2 // 2 (рядок "4" перетворюється в число, отримуємо 4 - 2 = 2)
"4px" - 2 // NaN (рядок "4px" не може бути перетворений в число, отримуємо NaN)
"  -9  " + 5 // "  -9  5" (конкатенація рядків)
"  -9  " - 5 // -14 (рядок "  -9  " перетворюється в число -9, отримуємо -9 - 5 = -14)
null + 1 // 1 (null перетворюється в 0, отримуємо 0 + 1 = 1)
undefined + 1 // NaN (undefined не може бути перетворено в число, отримуємо NaN)
" \t \n" - 2 // -2 (рядок " \t \n" перетворюється в 0, отримуємо 0 - 2 = -2)
